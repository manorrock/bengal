package com.veneni.m;

/**
 * The enum containing all of the Veneni M keywords.
 *
 * @author Manfred Riem (mriem@veneni.com)
 */
public enum Keyword {
    /**
     * The "object" keyword.
     */
    OBJECT,
    /**
     * The "method" keyword.
     */
    METHOD,
    /**
     * The "if" keyword.
     */
    IF,
    /**
     * The "then" keyword.
     */
    THEN,
    /**
     * The "else" keyword.
     */
    ELSE,
    /**
     * The "do" keyword.
     */
    DO,
    /**
     * The "while" keyword.
     */
    WHILE,
    /**
     * The "for" keyword.
     */
    FOR,
    /**
     * The "native" keyword.
     */
    NATIVE,
    /**
     * The "false" keyword.
     */
    FALSE,
    /**
     * The "true" keyword.
     */
    TRUE,
    /**
     * The "nil" keyword.
     */
    NIL
}
