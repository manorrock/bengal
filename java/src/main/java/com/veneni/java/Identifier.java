package com.veneni.java;

/**
 * An identifier.
 * 
 * @author Manfred Riem (mriem@veneni.com)
 */
public class Identifier {
    
    /**
     * Stores the string.
     */
    private String string;
    
    /**
     * Get the string.
     * 
     * @return the string.
     */
    public String getString() {
        return string;
    }
    
    /**
     * Set the string.
     * 
     * @param string the string.
     */
    public void setString(String string) {
        this.string = string;
    }
}
