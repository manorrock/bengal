package com.veneni.m;

/**
 * An operator (aka. the enum containing all of the Veneni M operators).
 *
 * @author Manfred Riem (mriem@veneni.com)
 */
public enum Operator {
    /**
     * The left parenthesis operator.
     */
    LEFT_PARENTHESIS,
    /**
     * The right parenthesis operator.
     */
    RIGHT_PARENTHESIS,
    /**
     * The semicolon operator.
     */
    SEMICOLON,
    /**
     * The left bracket operator.
     */
    LEFT_CURLY_BRACKET,
    /**
     * The right bracket operator.
     */
    RIGHT_CURLY_BRACKET,
    /**
     * The dot operator.
     */
    DOT,
    /**
     * The equals operator.
     */
    EQUALS
}
